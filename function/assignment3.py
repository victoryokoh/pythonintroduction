def mrs_yinka_budget():
    budget = 250000
    yam_cost = 4 * 500
    stationery_cost = 3 * 2000
    debt = 45000
    shop_rent = 25000 * 3
    total_expenses = yam_cost + stationery_cost + debt + shop_rent
    remaining_budget = budget - total_expenses
    print("Mrs. Yinka's remaining budget is:", remaining_budget)

mrs_yinka_budget()